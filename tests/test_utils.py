from hackytools.utils import ftime_ns

def test_ftime_ns():
    data = (
        (0, ('0', 'ns')),
        (10, ('10', 'ns')),
        (371, ('371', 'ns')),
        (5150, ('5150', '\u00b5s')),
        (58715, ('58715', '\u00b5s')),
        (987465, ('987465', '\u00b5s')),
        (3167431, ('3167431', 'ms')),
        (64744741, ('64744741', 'ms')),
        (943167497, ('943167497', 'ms')),
        (6468541351, ('6468541351', 's')),
        (68546438444, ('68546438444', 's')),
        (979779441123, ('979779441123', 's')),
        (7977941341123, ('7977941341123', 's')),
        (97977941341123, ('97977941341123', 's')),
    )

    for n, (before, after) in data:
        res = ftime_ns(n)
        assert res.startswith(before)
        assert res.endswith(after)
